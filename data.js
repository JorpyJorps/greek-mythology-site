export function getRelatedEntries(entryId) {
  const activeEntry = getEntryById(entryId);
  if (!activeEntry) {
    return entries.slice(0, 3);
  }

  const familyNeighbors = new Set(
    familyTree.links.flatMap((link) => {
      if (link.from === entryId) return [link.to];
      if (link.to === entryId) return [link.from];
      return [];
    })
  );

  return entries
    .filter((entry) => entry.id !== entryId)
    .map((entry) => {
      let score = 0;
      if (entry.category === activeEntry.category) score += 4;
      if (entry.home === activeEntry.home) score += 3;
      if (familyNeighbors.has(entry.id)) score += 5;
      if (entry.symbol === activeEntry.symbol) score += 2;
      if (
        activeEntry.story.toLowerCase().includes(entry.name.toLowerCase()) ||
        entry.story.toLowerCase().includes(activeEntry.name.toLowerCase())
      ) {
        score += 3;
      }

      return { entry, score };
    })
    .sort((left, right) => right.score - left.score)
    .filter((item) => item.score > 0)
    .map((item) => item.entry)
    .slice(0, 3);
}

export function getCompareCandidates(entryId) {
  return getRelatedEntries(entryId).slice(0, 2);
}

export function getMonsterProfile(entryId) {
  const entry = getEntryById(entryId);
  if (!entry || entry.category !== "monster") {
    return null;
  }

  const text = `${entry.story} ${entry.facts.join(" ")} ${entry.powers.join(" ")}`.toLowerCase();
  const danger =
    text.includes("underworld") || text.includes("hydra") || text.includes("chimera")
      ? 5
      : text.includes("maze") || text.includes("stone") || text.includes("giant")
        ? 4
        : 3;

  let weakness = "Use a clever plan and stay calm.";
  if (text.includes("stone")) weakness = "Use reflections and avoid a direct stare.";
  if (text.includes("maze")) weakness = "Bring a guide thread and move with a plan.";
  if (text.includes("sea")) weakness = "Watch the waves and wait for the right moment.";
  if (text.includes("dog")) weakness = "Stay brave and use strength or music to distract it.";

  return {
    danger,
    size: entry.powers.some((power) => power.toLowerCase().includes("giant")) ? "Giant" : "Large",
    weakness
  };
}

